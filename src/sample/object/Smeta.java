public class Smeta {

        private String name;
        private String cena;


        public Smeta(String name, String cena) {
            this.name = name;
            this.cena = cena;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public String getCena() {
            return cena;
        }

        public void setCena(String cena) {
            this.cena = cena;
        }


}
